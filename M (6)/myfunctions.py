
def area_circle(radius):
    """area of circle"""
    circle_answer=3.14*radius**2
    return circle_answer

def area_square(length):
    """area of square"""
    square_answer=length**2
    return square_answer

def area_right_triangle(base, height):
    """area of right triangle"""
    right_triangle_answer=(1/2)*base*height
    return right_triangle_answer